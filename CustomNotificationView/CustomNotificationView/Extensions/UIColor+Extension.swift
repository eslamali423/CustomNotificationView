import Foundation
import UIKit

extension UIColor {
    
    class var customGreenColor: UIColor {
        return UIColor(red: 67 / 255, green: 210 / 255, blue: 150 / 255, alpha: 1)
    }
    
    class var customOrangeColor: UIColor {
        return UIColor(red: 255 / 255, green: 164 / 255, blue: 0 / 255, alpha: 1)
    }
    
    class var customRedColor: UIColor {
        return UIColor(red: 252 / 255, green: 44 / 255, blue: 87 / 255, alpha: 1)
    }
    
}
